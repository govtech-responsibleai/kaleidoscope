"""
SQLAlchemy ORM models for the database schema.
"""

from datetime import datetime
from sqlalchemy import (
    Boolean, Column, Integer, String, Text, DateTime, ForeignKey,
    Enum, Float, UniqueConstraint, JSON, Index
)
from sqlalchemy.orm import relationship
from sqlalchemy.sql import text
import enum

from src.common.database.connection import Base


##### ENUMS #####

# Common 
class JobStatusEnum(enum.Enum):
    """Status of a generic job."""
    running = "running"
    completed = "completed"
    failed = "failed"
    paused = "paused" # For QAJob

# Scoring
class StatusEnum(enum.Enum):
    """Status for personas and questions."""
    pending = "pending"
    approved = "approved"
    rejected = "rejected"
    edited = "edited"

class JobTypeEnum(enum.Enum):
    """Type of generation job."""
    persona_generation = "persona_generation"
    question_generation = "question_generation"

class QuestionTypeEnum(enum.Enum):
    """Type of question."""
    typical = "typical"  # Typical use case questions
    edge = "edge"  # Edge case questions

class QuestionScopeEnum(enum.Enum):
    """Scope of question relative to knowledge base."""
    in_kb = "in_kb"  # Question about content within the knowledge base
    out_kb = "out_kb"  # Question about content outside the knowledge base

class InputStyleEnum(enum.Enum):
    """Input style for questions."""
    brief = "brief"
    regular = "regular"
    detailed = "detailed"

class QuestionSourceEnum(enum.Enum):
    """Source of question creation."""
    job_generated = "job_generated"  # Generated by a job
    uploaded = "uploaded"  # Uploaded via bulk import

class PersonaSourceEnum(enum.Enum):
    """Source of persona creation."""
    generated = "generated"  # Generated by LLM via a job
    nemotron = "nemotron"  # Sampled from Nemotron dataset


# Scoring
class QAJobTypeEnum(enum.Enum):
    """Type of generation job."""
    claim_scoring_full = "claim_scoring_full" # Handles answer generation -> process claims -> judge claims
    response_scoring_full = "response_scoring_full" # Handles answer generation -> judge answers
    claim_scoring_only = "claim_scoring_only" # Handles read claims -> judge claims
    response_scoring_only = "response_scoring_only" # Handles read answers -> judge claims

class QAJobStageEnum(enum.Enum):
    """Stages of a QAJob"""
    starting = "starting"
    generating_answers = "generating_answers"
    processing_answers = "processing_answers" # Any type of answer processing, e.g. extracting claims, or processing full responses
    scoring_answers = "scoring_answers" # Any type of answer scoring, e.g. claim scoring or response scoring
    completed = "completed" # Any type of answer scoring, e.g. claim scoring or response scoring

##### AUTH #####

class User(Base):
    """User for authentication."""
    __tablename__ = "users"

    id = Column(Integer, primary_key=True, index=True)
    username = Column(String, unique=True, nullable=False, index=True)
    hashed_password = Column(String, nullable=False)
    is_active = Column(Boolean, default=True, nullable=False)
    is_admin = Column(Boolean, default=False, nullable=False)
    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)

    # Relationships
    targets = relationship("Target", back_populates="owner")
    judges = relationship("Judge", back_populates="owner")

    def __repr__(self):
        return f"<User(id={self.id}, username='{self.username}', is_admin={self.is_admin})>"


##### GENERAL #####

class Target(Base):
    """Target application for evaluation."""
    __tablename__ = "targets"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(Integer, ForeignKey("users.id", ondelete="CASCADE"), nullable=True, index=True)
    name = Column(String, nullable=False, index=True)
    agency = Column(String, nullable=True)
    purpose = Column(Text, nullable=True)
    target_users = Column(Text, nullable=True)
    api_endpoint = Column(String, nullable=True)
    endpoint_type = Column(String, nullable=True)  # "http", or extension types
    endpoint_config = Column(JSON, nullable=True)  # Type-specific config
    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow, nullable=False)

    # Relationships
    owner = relationship("User", back_populates="targets")
    jobs = relationship("Job", back_populates="target", cascade="all, delete-orphan")
    personas = relationship("Persona", back_populates="target", cascade="all, delete-orphan")
    questions = relationship("Question", back_populates="target", cascade="all, delete-orphan")
    kb_documents = relationship("KnowledgeBaseDocument", back_populates="target", cascade="all, delete-orphan")
    web_documents = relationship("WebDocument", back_populates="target", cascade="all, delete-orphan")
    snapshots = relationship("Snapshot", back_populates="target", cascade="all, delete-orphan")
    custom_rubrics = relationship("TargetRubric", back_populates="target", cascade="all, delete-orphan")
    http_auth_secret = relationship(
        "TargetHttpAuthSecret",
        back_populates="target",
        cascade="all, delete-orphan",
        uselist=False,
    )

    def __repr__(self):
        return f"<Target(id={self.id}, name='{self.name}', user_id={self.user_id})>"


class TargetHttpAuthSecret(Base):
    """Encrypted HTTP auth secret stored outside target.endpoint_config."""

    __tablename__ = "target_http_auth_secrets"

    id = Column(Integer, primary_key=True, index=True)
    target_id = Column(Integer, ForeignKey("targets.id", ondelete="CASCADE"), nullable=False, unique=True, index=True)
    encrypted_secret = Column(Text, nullable=False)
    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow, nullable=False)

    target = relationship("Target", back_populates="http_auth_secret")

    def __repr__(self):
        return f"<TargetHttpAuthSecret(target_id={self.target_id})>"


##### QUERY GENERATION #####

class Job(Base):
    """Generation job for personas or questions."""
    __tablename__ = "jobs"

    id = Column(Integer, primary_key=True, index=True)
    target_id = Column(Integer, ForeignKey("targets.id", ondelete="CASCADE"), nullable=False, index=True)
    type = Column(Enum(JobTypeEnum), nullable=False, index=True)
    persona_id = Column(Integer, ForeignKey("personas.id", ondelete="SET NULL"), nullable=True, index=True)
    count_requested = Column(Integer, nullable=False)
    model_used = Column(String, nullable=False)
    generation_prompt = Column(Text, nullable=True)
    status = Column(Enum(JobStatusEnum), default=JobStatusEnum.running, nullable=False, index=True)
    prompt_tokens = Column(Integer, default=0)
    completion_tokens = Column(Integer, default=0)
    total_cost = Column(Float, default=0.0)
    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow, nullable=False)

    # Relationships
    target = relationship("Target", back_populates="jobs")
    persona = relationship("Persona", foreign_keys=[persona_id])
    personas_generated = relationship("Persona", back_populates="job", foreign_keys="Persona.job_id")
    questions_generated = relationship("Question", back_populates="job")

    def __repr__(self):
        return f"<Job(id={self.id}, type={self.type.value}, status={self.status.value}, cost=${self.total_cost:.4f})>"


class Persona(Base):
    """Generated or sampled persona for a target application."""
    __tablename__ = "personas"

    id = Column(Integer, primary_key=True, index=True)
    source = Column(Enum(PersonaSourceEnum), default=PersonaSourceEnum.generated, nullable=False, index=True)
    job_id = Column(Integer, ForeignKey("jobs.id", ondelete="CASCADE"), nullable=True, index=True)  # Required if source=generated, else NULL
    target_id = Column(Integer, ForeignKey("targets.id", ondelete="CASCADE"), nullable=False, index=True)
    title = Column(String, nullable=False)
    info = Column(Text, nullable=True)
    style = Column(Text, nullable=True)
    use_case = Column(Text, nullable=True)
    status = Column(Enum(StatusEnum), default=StatusEnum.pending, nullable=False, index=True)
    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow, nullable=False)

    # Relationships
    job = relationship("Job", back_populates="personas_generated", foreign_keys=[job_id])
    target = relationship("Target", back_populates="personas")
    questions = relationship("Question", back_populates="persona", cascade="all, delete-orphan")

    # Unique constraint: title must be unique per target
    __table_args__ = (
        UniqueConstraint('target_id', 'title', name='uix_target_persona_title'),
    )

    def __repr__(self):
        return f"<Persona(id={self.id}, title='{self.title}', status={self.status.value})>"


class Question(Base):
    """Generated or uploaded question for a persona."""
    __tablename__ = "questions"

    id = Column(Integer, primary_key=True, index=True)
    source = Column(Enum(QuestionSourceEnum), default=QuestionSourceEnum.job_generated, nullable=False, index=True)
    job_id = Column(Integer, ForeignKey("jobs.id", ondelete="CASCADE"), nullable=True, index=True)  # Required if source=job_generated, else NULL
    persona_id = Column(Integer, ForeignKey("personas.id", ondelete="CASCADE"), nullable=True, index=True)  # Required if source=job_generated, else optional
    target_id = Column(Integer, ForeignKey("targets.id", ondelete="CASCADE"), nullable=False, index=True)
    orig_id = Column(String, nullable=True, index=True)  # Original ID from user's file (for uploaded questions)
    text = Column(Text, nullable=False)
    type = Column(Enum(QuestionTypeEnum), nullable=True, index=True)  # typical or edge; nullable for uploaded questions
    scope = Column(Enum(QuestionScopeEnum), nullable=True, index=True)  # in_kb or out_kb; nullable for uploaded questions
    input_style = Column(Enum(InputStyleEnum), nullable=True)  # brief, regular, detailed
    status = Column(Enum(StatusEnum), default=StatusEnum.pending, nullable=False, index=True)
    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow, nullable=False)

    # Relationships
    job = relationship("Job", back_populates="questions_generated")
    persona = relationship("Persona", back_populates="questions")
    target = relationship("Target", back_populates="questions")
    answers = relationship("Answer", back_populates="question", cascade="all, delete-orphan")
    qa_jobs = relationship("QAJob", back_populates="question", cascade="all, delete-orphan")

    def __repr__(self):
        return f"<Question(id={self.id}, persona_id={self.persona_id}, type={self.type.value}, scope={self.scope.value}, status={self.status.value})>"


class Answer(Base):
    """Generated answer from AIBots API for a question."""
    __tablename__ = "answers"

    id = Column(Integer, primary_key=True, index=True)
    question_id = Column(Integer, ForeignKey("questions.id", ondelete="CASCADE"), nullable=False, index=True)
    snapshot_id = Column(Integer, ForeignKey("snapshots.id", ondelete="CASCADE"), nullable=False, index=True)

    # AIBots identifiers
    chat_id = Column(String, nullable=True)
    message_id = Column(String, nullable=True)

    # Important fields (extracted for easy querying)
    answer_content = Column(Text, nullable=False)
    system_prompt = Column(Text, nullable=True)
    model = Column(String, nullable=True)
    guardrails = Column(JSON, nullable=True)
    rag_citations = Column(JSON, nullable=True)

    # Full raw response for traceability
    raw_response = Column(JSON, nullable=True)

    # Annotation selection
    is_selected_for_annotation = Column(Boolean, default=False, nullable=False, index=True)

    created_at = Column(DateTime, default=datetime.utcnow, nullable=False)

    # Relationships
    qa_jobs = relationship("QAJob", back_populates="answer", cascade="all, delete")
    snapshot = relationship("Snapshot", back_populates="answers")
    question = relationship("Question", back_populates="answers")
    claims = relationship("AnswerClaim", back_populates="answer", cascade="all, delete-orphan")
    scores = relationship("AnswerScore", back_populates="answer", cascade="all, delete-orphan")
    annotations = relationship("Annotation", back_populates="answer", cascade="all, delete-orphan")

    # Unique constraint: one answer per question per snapshot
    __table_args__ = (
        UniqueConstraint('question_id', 'snapshot_id', name='uix_question_snapshot_answer'),
    )

    def __repr__(self):
        return f"<Answer(id={self.id}, question_id={self.question_id}, snapshot_id={self.snapshot_id})>"


